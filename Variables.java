/* What is Variables ?
*  Variables are a way to store information in our computer. Are used
* to store the value of the data in it while the java program is executed
*
* In Short
* Variables --> Containers --> Store data value --> When program is executed
* */
public class Variables {
    // The below line which we have type is called declaration of a Statement
    public static void main( String [] args) {
        int myFirstNumber = 6;      // int --> datatype || myFirstNumber --> Variable || 5   --> literal value //
        System.out.println(myFirstNumber);
        System.out.println("myFirstNumber");

        int  mySecondNumber = (10 + 6) + (2 * 10);  //Java operator
        System.out.print(mySecondNumber);

    }
}
